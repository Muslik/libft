#include "libft.h"
#include "ft_printf.h"

int	ft_strisnumeric(char *str)
{
	if (str[0] == '-' && str[1])
		++str;
	while (*str)
	{
		if (!(*str >= '0' && *str <= '9'))
			return (0);
		++str;
	}
	return (1);
}
